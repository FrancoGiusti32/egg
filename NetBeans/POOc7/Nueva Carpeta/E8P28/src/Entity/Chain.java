package Entity;

public class Chain {
    
    public String chain;
    public int lenght;

    public Chain() {
    }

    public String getChain() {
        return chain;
    }

    public int getLenght() {
        return lenght;
    }

    public void setChain(String chain) {
        this.chain = chain;
    }

    public void setLenght(int lenght) {
        this.lenght = lenght;
    }
    
    
}
