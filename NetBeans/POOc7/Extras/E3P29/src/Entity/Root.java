package Entity;

public class Root {

    public int a;
    public int b;
    public int c;

    public Root(int a, int b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public Root() {
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public int getC() {
        return c;
    }

    public void setA(int a) {
        this.a = a;
    }

    public void setB(int b) {
        this.b = b;
    }

    public void setC(int c) {
        this.c = c;
    }

}
